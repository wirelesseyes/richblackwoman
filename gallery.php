    <script>
        var fst_content = document.getElementById("fst_content");
        var nav_content = document.getElementById("nav-content");
        var nav_ul = document.getElementById("nav-ul");
        var menu_icon = document.getElementById("menu-icon");
        const closeNavbar = document.querySelector(".closeNavigation");

        const menuBtn = document.querySelector(".menu-icon span");
        const searchBtn = document.querySelector(".search-icon");
        const cancelBtn = document.querySelector(".cancel-icon");
        const items = document.querySelector(".nav-items");
        const form = document.querySelector(".search-box");

        closeNavbar.onclick = function () {
            menu_icon.classList.remove("activeMenu_icon");
            nav_ul.classList.remove("activeNav_ul");
        };

        menu_icon.onclick = function () {
            menu_icon.classList.toggle("activeMenu_icon");
            nav_ul.classList.toggle("activeNav_ul");
            searchBtn.classList.remove("hide");
            cancelBtn.classList.remove("show");
            form.classList.remove("active");
            cancelBtn.style.color = "#ff3d00";
        };

        cancelBtn.onclick = () => {
            searchBtn.classList.remove("hide");
            cancelBtn.classList.remove("show");
            form.classList.remove("active");
            cancelBtn.style.color = "#ff3d00";
        };
        searchBtn.onclick = () => {
            form.classList.add("active");
            searchBtn.classList.add("hide");
            cancelBtn.classList.add("show");
            menu_icon.classList.remove("activeMenu_icon");
            nav_ul.classList.remove("activeNav_ul");
        };
        // scrolling effect
        var nav = document.querySelector("nav");
        window.addEventListener("scroll", function (e) {
            const lastPosition = window.scrollY;
            if (lastPosition > 140) {
                nav.classList.add("sticky-nav-color");
                nav.classList.remove("TranskNav");
            } else if (nav.classList.contains("sticky-nav-color")) {
                nav.classList.remove("sticky-nav-color");
                nav.classList.add("TranskNav");
            } else {
                nav.classList.remove("sticky-nav-color");
                nav.classList.add("TranskNav");
            }
        });
    </script>

